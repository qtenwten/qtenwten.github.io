export function normalizeSeoPath(path = '/') {
  const cleanPath = path.replace(/^\/(ru|en)(?=\/|$)/, '') || '/'
  return cleanPath !== '/' && cleanPath.endsWith('/') ? cleanPath.slice(0, -1) : cleanPath
}

export function getLocalizedRoutePath(language, path = '/') {
  const cleanPath = normalizeSeoPath(path)
  if (cleanPath === '/') return `/${language}/`
  return `/${language}${cleanPath}`
}

export function getLocalizedRouteUrl(language, path = '/') {
  return `https://qsen.ru${getLocalizedRoutePath(language, path)}`
}

export function getRouteSeo(language, path = '/') {
  const cleanPath = normalizeSeoPath(path)
  const page = ROUTE_SEO[cleanPath]?.[language] || ROUTE_SEO['/'][language]
  return {
    robots: 'index,follow',
    includeInSitemap: true,
    ...page,
  }
}

export function getAllLocalizedSeoPages() {
  return Object.entries(ROUTE_SEO).flatMap(([path, localized]) => (
    ['ru', 'en'].map((language) => ({
      language,
      path,
      route: getLocalizedRoutePath(language, path),
      url: getLocalizedRouteUrl(language, path),
      locale: language === 'en' ? 'en_US' : 'ru_RU',
      ...getRouteSeo(language, path),
    }))
  ))
}
